    public void ghiFile(){
        String errConnect = Connect.getConnection();
        if(!errConnect.equals("")){
            JOptionPane.showMessageDialog(this, errConnect, "Lỗi kết nối", JOptionPane.ERROR_MESSAGE); 
            return;
        }
        String sql = "{call GhiLichsuDoanSo(?,?,?,?,?)}";
        CallableStatement a;
        String kq;
        try {
            a = Connect.conn.prepareCall(sql);
            a.setString(1, "Game đoán số");
            a.setInt(2, dem);
            a.setInt(3, diem);
            a.setString(4, Connect.UserID);
            a.registerOutParameter(5, java.sql.Types.NVARCHAR);
            a.execute();
            kq = a.getString(5);
            JOptionPane.showMessageDialog(this, kq, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            a.close();
            Connect.conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
