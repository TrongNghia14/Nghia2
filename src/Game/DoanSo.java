    private void txtDoanSoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDoanSoActionPerformed
        int soMay = Integer.parseInt(lblRandom.getText());int soNguoi = Integer.parseInt(txtDoanSo.getText());
        while(true){
            if(soNguoi == soMay){
                dem++;lblKetQua.setText("Correct!!!");lblDoan.setText(Integer.toString(dem));lblDiem.setText(Integer.toString(diem));
                ghiFile();
                int chon = JOptionPane.YES_OPTION;
                if(diem != 0)
                    chon = JOptionPane.showConfirmDialog(null, "Bạn đoán đúng rồi!!!\nBạn chơi nữa không?", 
                        "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if(diem == 0)
                    chon = JOptionPane.showConfirmDialog(null, "Bạn thua rồi!!!\nBạn chơi nữa không?", 
                        "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if(chon == JOptionPane.NO_OPTION)this.dispose();else khoiDong();break;
            }
            else if(soNguoi < soMay){
                dem++;lblKetQua.setText("<html><b>Lớn hơn nha</b>");txtDoanSo.setText("");txtDoanSo.requestFocus();
                if(diem == 0){
                    JOptionPane.showMessageDialog(null, "Bạn đã thua", 
                            "Thông báo", JOptionPane.YES_OPTION);
                    ghiFile();
                    int chon = JOptionPane.YES_OPTION;
                    chon = JOptionPane.showConfirmDialog(null, "Bạn thua rồi!!!\nBạn chơi nữa không?", 
                            "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    if(chon == JOptionPane.NO_OPTION) System.exit(0);else khoiDong();break;
                }
                else{lblDoan.setText(Integer.toString(dem));diem = diem - 10;lblDiem.setText(Integer.toString(diem));}break;
            }else{
                dem++;lblKetQua.setText("<html><b>Nhỏ hơn nha</b>");txtDoanSo.setText("");txtDoanSo.requestFocus();
                if(diem == 0){
                    JOptionPane.showMessageDialog(null, "Bạn đã thua", "Thông báo", JOptionPane.YES_OPTION);
                    ghiFile(); int chon = JOptionPane.YES_OPTION;
                    chon = JOptionPane.showConfirmDialog(null, "Bạn thua rồi!!!\nBạn chơi nữa không?", 
                            "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    if(chon == JOptionPane.NO_OPTION)System.exit(0);else khoiDong();break;
                }
                else{lblDoan.setText(Integer.toString(dem));diem = diem - 10;lblDiem.setText(Integer.toString(diem));} break;}}     
    }//GEN-LAST:event_txtDoanSoActionPerformed
    
    
